    childRoutes(path) {
        let childRoutes;
        for (let i = 0; i < routes.config.length; i++) {
            if (routes.config[i].path !== path) {
                continue;
            }
            childRoutes = routes.config[i].childRoutes;
            for (let j = 0; j < childRoutes.length; j++) {
                childRoutes[j].component = childRoutes[j].component();
            }
            break;
        }
        return childRoutes ? childRoutes : [];
    }
